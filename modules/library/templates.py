def get_subject_icon(subject_name: str) -> str:
    """جلب أيقونة للمادة"""
    if subject_name in SUBJECT_ICONS:
        return SUBJECT_ICONS[subject_name]
    
    for key, icon in SUBJECT_ICONS.items():
        if key in subject_name or subject_name in key:
            return icon
    
    return '📚'


def get_random_color() -> str:
    """جلب لون عشوائي للمادة"""
    import random
    return random.choice(SUBJECT_COLORS)


def get_stage_grades(stage_name: str) -> list:
    """جلب صفوف مرحلة معينة"""
    return GRADES_BY_STAGE.get(stage_name, [])


def get_terms_for_stage(stage_name: str) -> list:
    """جلب الترمات المناسبة لمرحلة"""
    if stage_name == 'جامعي':
        return [t for t in TERMS if t['order'] in [1, 2, 0]]
    else:
        return [t for t in TERMS if t['order'] in [1, 2, 0]]


def get_colleges_by_category() -> dict:
    """جلب الكليات مقسمة حسب التخصص"""
    categories = {}
    for college in COLLEGES:
        cat = college.get('category', 'متنوع')
        if cat not in categories:
            categories[cat] = []
        categories[cat].append(college)
    return categories